std::shared_ptr<IMemtrack> IMemtrack::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IMemtrack>(interface);
  }
  return ::ndk::SharedRefBase::make<BpMemtrack>(binder);
}

binder_status_t IMemtrack::writeToParcel(AParcel* parcel, const std::shared_ptr<IMemtrack>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t IMemtrack::readFromParcel(const AParcel* parcel, std::shared_ptr<IMemtrack>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IMemtrack::fromBinder(binder);
  return STATUS_OK;
}
bool IMemtrack::setDefaultImpl(const std::shared_ptr<IMemtrack>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IMemtrack::default_impl);
  if (impl) {
    IMemtrack::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IMemtrack>& IMemtrack::getDefaultImpl() {
  return IMemtrack::default_impl;
}
