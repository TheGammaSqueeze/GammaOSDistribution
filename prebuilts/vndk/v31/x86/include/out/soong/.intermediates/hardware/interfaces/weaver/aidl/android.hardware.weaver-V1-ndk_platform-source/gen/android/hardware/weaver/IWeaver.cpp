std::shared_ptr<IWeaver> IWeaver::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IWeaver>(interface);
  }
  return ::ndk::SharedRefBase::make<BpWeaver>(binder);
}

