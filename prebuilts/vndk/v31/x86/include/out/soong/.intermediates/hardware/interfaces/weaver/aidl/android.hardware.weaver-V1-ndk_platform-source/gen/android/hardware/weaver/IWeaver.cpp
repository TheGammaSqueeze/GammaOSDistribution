binder_status_t IWeaver::readFromParcel(const AParcel* parcel, std::shared_ptr<IWeaver>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IWeaver::fromBinder(binder);
  return STATUS_OK;
}
bool IWeaver::setDefaultImpl(const std::shared_ptr<IWeaver>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IWeaver::default_impl);
  if (impl) {
    IWeaver::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IWeaver>& IWeaver::getDefaultImpl() {
  return IWeaver::default_impl;
}
