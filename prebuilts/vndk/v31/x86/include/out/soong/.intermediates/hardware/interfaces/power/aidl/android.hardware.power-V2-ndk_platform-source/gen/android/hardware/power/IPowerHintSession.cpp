bool IPowerHintSession::setDefaultImpl(const std::shared_ptr<IPowerHintSession>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IPowerHintSession::default_impl);
  if (impl) {
    IPowerHintSession::default_impl = impl;
    return true;
  }
  return false;
}
