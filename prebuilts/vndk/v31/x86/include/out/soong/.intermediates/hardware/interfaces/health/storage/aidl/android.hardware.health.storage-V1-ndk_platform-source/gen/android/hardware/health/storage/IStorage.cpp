std::shared_ptr<IStorage> IStorage::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IStorage>(interface);
  }
  return ::ndk::SharedRefBase::make<BpStorage>(binder);
}

binder_status_t IStorage::writeToParcel(AParcel* parcel, const std::shared_ptr<IStorage>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t IStorage::readFromParcel(const AParcel* parcel, std::shared_ptr<IStorage>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IStorage::fromBinder(binder);
  return STATUS_OK;
}
bool IStorage::setDefaultImpl(const std::shared_ptr<IStorage>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IStorage::default_impl);
  if (impl) {
    IStorage::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IStorage>& IStorage::getDefaultImpl() {
  return IStorage::default_impl;
}
