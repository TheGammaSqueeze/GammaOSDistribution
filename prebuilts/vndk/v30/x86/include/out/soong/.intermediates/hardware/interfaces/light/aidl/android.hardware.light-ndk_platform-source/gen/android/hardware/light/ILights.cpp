std::shared_ptr<ILights> ILights::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<ILights>(interface);
  }
  return ::ndk::SharedRefBase::make<BpLights>(binder);
}

binder_status_t ILights::writeToParcel(AParcel* parcel, const std::shared_ptr<ILights>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t ILights::readFromParcel(const AParcel* parcel, std::shared_ptr<ILights>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = ILights::fromBinder(binder);
  return STATUS_OK;
}
bool ILights::setDefaultImpl(std::shared_ptr<ILights> impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!ILights::default_impl);
  if (impl) {
    ILights::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<ILights>& ILights::getDefaultImpl() {
  return ILights::default_impl;
}
