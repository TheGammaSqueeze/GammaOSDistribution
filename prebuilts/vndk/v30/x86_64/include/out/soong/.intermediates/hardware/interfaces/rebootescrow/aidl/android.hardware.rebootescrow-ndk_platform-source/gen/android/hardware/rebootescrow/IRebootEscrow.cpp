binder_status_t IRebootEscrow::readFromParcel(const AParcel* parcel, std::shared_ptr<IRebootEscrow>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IRebootEscrow::fromBinder(binder);
  return STATUS_OK;
}
bool IRebootEscrow::setDefaultImpl(std::shared_ptr<IRebootEscrow> impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IRebootEscrow::default_impl);
  if (impl) {
    IRebootEscrow::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IRebootEscrow>& IRebootEscrow::getDefaultImpl() {
  return IRebootEscrow::default_impl;
}
