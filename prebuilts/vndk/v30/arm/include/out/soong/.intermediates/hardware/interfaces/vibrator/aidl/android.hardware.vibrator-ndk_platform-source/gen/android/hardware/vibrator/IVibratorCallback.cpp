std::shared_ptr<IVibratorCallback> IVibratorCallback::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IVibratorCallback>(interface);
  }
  return ::ndk::SharedRefBase::make<BpVibratorCallback>(binder);
}

binder_status_t IVibratorCallback::writeToParcel(AParcel* parcel, const std::shared_ptr<IVibratorCallback>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t IVibratorCallback::readFromParcel(const AParcel* parcel, std::shared_ptr<IVibratorCallback>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IVibratorCallback::fromBinder(binder);
  return STATUS_OK;
}
bool IVibratorCallback::setDefaultImpl(std::shared_ptr<IVibratorCallback> impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IVibratorCallback::default_impl);
  if (impl) {
    IVibratorCallback::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IVibratorCallback>& IVibratorCallback::getDefaultImpl() {
  return IVibratorCallback::default_impl;
}
