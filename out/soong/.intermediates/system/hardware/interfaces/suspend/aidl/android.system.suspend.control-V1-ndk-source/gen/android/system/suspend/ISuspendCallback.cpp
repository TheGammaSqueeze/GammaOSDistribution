std::shared_ptr<ISuspendCallback> ISuspendCallback::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<ISuspendCallback>(interface);
  }
  return ::ndk::SharedRefBase::make<BpSuspendCallback>(binder);
}

binder_status_t ISuspendCallback::writeToParcel(AParcel* parcel, const std::shared_ptr<ISuspendCallback>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t ISuspendCallback::readFromParcel(const AParcel* parcel, std::shared_ptr<ISuspendCallback>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = ISuspendCallback::fromBinder(binder);
  return STATUS_OK;
}
bool ISuspendCallback::setDefaultImpl(const std::shared_ptr<ISuspendCallback>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!ISuspendCallback::default_impl);
  if (impl) {
    ISuspendCallback::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<ISuspendCallback>& ISuspendCallback::getDefaultImpl() {
  return ISuspendCallback::default_impl;
}
