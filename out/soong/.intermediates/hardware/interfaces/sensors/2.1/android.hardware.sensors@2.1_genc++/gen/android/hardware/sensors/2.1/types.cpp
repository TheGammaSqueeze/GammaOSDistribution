::android::status_t readEmbeddedFromParcel(
        const SensorInfo &obj,
        const ::android::hardware::Parcel &parcel,
        size_t parentHandle,
        size_t parentOffset) {
    ::android::status_t _hidl_err = ::android::OK;

    _hidl_err = ::android::hardware::readEmbeddedFromParcel(
            const_cast<::android::hardware::hidl_string &>(obj.name),
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, name));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::readEmbeddedFromParcel(
            const_cast<::android::hardware::hidl_string &>(obj.vendor),
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, vendor));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::readEmbeddedFromParcel(
            const_cast<::android::hardware::hidl_string &>(obj.typeAsString),
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, typeAsString));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::readEmbeddedFromParcel(
            const_cast<::android::hardware::hidl_string &>(obj.requiredPermission),
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, requiredPermission));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    return _hidl_err;
}

::android::status_t writeEmbeddedToParcel(
        const SensorInfo &obj,
        ::android::hardware::Parcel *parcel,
        size_t parentHandle,
        size_t parentOffset) {
    ::android::status_t _hidl_err = ::android::OK;

    _hidl_err = ::android::hardware::writeEmbeddedToParcel(
            obj.name,
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, name));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::writeEmbeddedToParcel(
            obj.vendor,
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, vendor));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::writeEmbeddedToParcel(
            obj.typeAsString,
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, typeAsString));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    _hidl_err = ::android::hardware::writeEmbeddedToParcel(
            obj.requiredPermission,
            parcel,
            parentHandle,
            parentOffset + offsetof(::android::hardware::sensors::V2_1::SensorInfo, requiredPermission));

    if (_hidl_err != ::android::OK) { return _hidl_err; }

    return _hidl_err;
}

