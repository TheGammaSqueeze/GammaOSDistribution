std::shared_ptr<IADBRootService> IADBRootService::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IADBRootService>(interface);
  }
  return ::ndk::SharedRefBase::make<BpADBRootService>(binder);
}

binder_status_t IADBRootService::writeToParcel(AParcel* parcel, const std::shared_ptr<IADBRootService>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t IADBRootService::readFromParcel(const AParcel* parcel, std::shared_ptr<IADBRootService>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IADBRootService::fromBinder(binder);
  return STATUS_OK;
}
bool IADBRootService::setDefaultImpl(const std::shared_ptr<IADBRootService>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IADBRootService::default_impl);
  if (impl) {
    IADBRootService::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IADBRootService>& IADBRootService::getDefaultImpl() {
  return IADBRootService::default_impl;
}
