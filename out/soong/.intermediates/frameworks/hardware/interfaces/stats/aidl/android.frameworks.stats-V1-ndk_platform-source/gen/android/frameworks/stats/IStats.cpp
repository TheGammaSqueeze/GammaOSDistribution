std::shared_ptr<IStats> IStats::fromBinder(const ::ndk::SpAIBinder& binder) {
  if (!AIBinder_associateClass(binder.get(), _g_aidl_clazz)) { return nullptr; }
  std::shared_ptr<::ndk::ICInterface> interface = ::ndk::ICInterface::asInterface(binder.get());
  if (interface) {
    return std::static_pointer_cast<IStats>(interface);
  }
  return ::ndk::SharedRefBase::make<BpStats>(binder);
}

binder_status_t IStats::writeToParcel(AParcel* parcel, const std::shared_ptr<IStats>& instance) {
  return AParcel_writeStrongBinder(parcel, instance ? instance->asBinder().get() : nullptr);
}
binder_status_t IStats::readFromParcel(const AParcel* parcel, std::shared_ptr<IStats>* instance) {
  ::ndk::SpAIBinder binder;
  binder_status_t status = AParcel_readStrongBinder(parcel, binder.getR());
  if (status != STATUS_OK) return status;
  *instance = IStats::fromBinder(binder);
  return STATUS_OK;
}
bool IStats::setDefaultImpl(const std::shared_ptr<IStats>& impl) {
  // Only one user of this interface can use this function
  // at a time. This is a heuristic to detect if two different
  // users in the same process use this function.
  assert(!IStats::default_impl);
  if (impl) {
    IStats::default_impl = impl;
    return true;
  }
  return false;
}
const std::shared_ptr<IStats>& IStats::getDefaultImpl() {
  return IStats::default_impl;
}
